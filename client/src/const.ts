// Generate login URL at runtime so redirect URI reflects the current origin.
export const getLoginUrl = () => {
  const oauthPortalUrl = import.meta.env.VITE_OAUTH_PORTAL_URL;
  const appId = import.meta.env.VITE_APP_ID;

  // Fallback: redirect to demo login if OAuth is not configured
  if (!oauthPortalUrl || oauthPortalUrl === "undefined" || !appId || appId === "undefined") {
    return "/api/demo-login?user=demo-admin";
  }

  const redirectUri = `${window.location.origin}/api/oauth/callback`;
  const state = btoa(redirectUri);

  try {
    const url = new URL(`${oauthPortalUrl}/app-auth`);
    url.searchParams.set("appId", appId);
    url.searchParams.set("redirectUri", redirectUri);
    url.searchParams.set("state", state);
    url.searchParams.set("type", "signIn");
    return url.toString();
  } catch {
    // If URL construction fails, fallback to demo login
    return "/api/demo-login?user=demo-admin";
  }
};
